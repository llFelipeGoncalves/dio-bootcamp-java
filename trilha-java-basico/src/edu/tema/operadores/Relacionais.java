package edu.tema.operadores;

public class Relacionais {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        boolean simNao = a == b; // iguais
        
        System.out.println(simNao); 

        simNao = a != b; // diferente
        
        System.out.println(simNao);

        simNao = a >= b; // maior ou igual
        
        System.out.println(simNao);

    }
}
